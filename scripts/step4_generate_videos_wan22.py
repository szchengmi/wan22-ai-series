def _find_wan22_models():
    """查找 Wan2.2 模型文件（Dataset 挂载 或 本地目录）"""
    search_paths = []
    for dataset_dir in ["/kaggle/input/datasets/saysnkaggle/wan2-2-5b-q4-gguf",
                        "/kaggle/input/datasets/saysnkaggle/newdataset",
                        "/kaggle/input/newdataset"]:
        if os.path.isdir(dataset_dir):
            search_paths.append(f"{dataset_dir}/model")
            search_paths.append(f"{dataset_dir}/models")
    search_paths.extend([
        "/kaggle/working/wan22-ai-series/models",
        "/kaggle/working/models",
    ])

    result = {"unet": None, "clip": None, "vae": None}
    for base in search_paths:
        if not os.path.isdir(base):
            continue
        if not result["unet"]:
            for name in ["Wan2.2-TI2V-5B-Q4_K_M.gguf", "Wan2.2-TI2V-5B.gguf"]:
                if os.path.isfile(f"{base}/{name}"):
                    result["unet"] = f"{base}/{name}"
                    break
        if not result["clip"]:
            for name in ["umt5-xxl-encoder-Q4_K_M.gguf", "umt5-xxl-encoder.gguf"]:
                if os.path.isfile(f"{base}/{name}"):
                    result["clip"] = f"{base}/{name}"
                    break
        if not result["vae"]:
            for name in ["Wan2.2_VAE.safetensors", "Wan2.2_VAE.pth"]:
                if os.path.isfile(f"{base}/{name}"):
                    result["vae"] = f"{base}/{name}"
                    break
    return result


def main(storyboard=None):
    log("=" * 50)
    log("Step 4: 视频生成 (Wan2.2-TI2V-5B)")
    log("=" * 50)

    dirs = get_dirs(EPISODE_NUM)
    total = sum(len(s.get("shots", [])) for s in storyboard.get("scenes", []))

    # 模型路径（支持 Dataset 挂载）
    models = _find_wan22_models()
    model_path = models["unet"]
    clip_path = models["clip"]
    vae_path = models["vae"]

    # 检查模型
    for name, path in [("UNET", model_path), ("CLIP", clip_path), ("VAE", vae_path)]:
        if not path:
            log(f"  ❌ {name} 模型未找到")
            return
        size_mb = os.path.getsize(path) / 1e6
        log(f"  ✅ {name}: {os.path.basename(path)} ({size_mb:.0f}MB)")

    # 启动 ComfyUI
    if not _start_comfyui():
        log("❌ ComfyUI 启动失败")
        return

    # Wan2.2 默认参数
    w = WAN22_DEFAULTS["width"]
    h = WAN22_DEFAULTS["height"]
    fps = WAN22_DEFAULTS["fps"]
    steps = WAN22_DEFAULTS["steps"]
    cfg = WAN22_DEFAULTS["cfg"]
    sampler = WAN22_DEFAULTS["sampler"]
    scheduler = WAN22_DEFAULTS["scheduler"]
    shift = WAN22_DEFAULTS["shift"]
    num_frames = WAN22_DEFAULTS["frames_per_shot"]

    log(f"参数: {w}x{h} | {num_frames}f | {steps}步 | CFG={cfg} | {sampler}/{scheduler}")

    # 检查 ComfyUI 是否识别到模型（通过 system_stats 验证）
    try:
        import urllib.request
        stats = json.loads(urllib.request.urlopen(f"{COMFYUI_URL}/system_stats", timeout=5).read())
        devices = stats.get("devices", [])
        log(f"  ComfyUI devices: {[d.get('name','?') for d in devices]}")
    except Exception as e:
        log(f"  ⚠️ system_stats 失败: {e}")

    count = 0
    for scene in storyboard.get("scenes", []):
        for shot in scene.get("shots", []):
            count += 1
            sid = shot["shot_id"]
            ep = storyboard.get("episode", 1)
            out = f"{dirs['videos']}/ep{ep:02d}_{scene['scene_id']}_{sid}.mp4"

            if os.path.exists(out) and os.path.getsize(out) > 100000:
                log(f"  [{count}/{total}] {sid} 跳过(已存在)")
                continue

            # 构建视频 prompt（从分镜 prompt + 动作描述）
            video_prompt = f"{shot.get('prompt', '')}, smooth motion, cinematic, high quality"
            neg_prompt = shot.get("negative_prompt", "blurry, distorted, low quality, static, motionless")

            seed = shot.get("seed", -1)
            if seed < 0:
                seed = WAN22_DEFAULTS["seed"]

            workflow = _build_wan22_workflow(
                positive_prompt=video_prompt,
                negative_prompt=neg_prompt,
                model_path=model_path,
                clip_path=clip_path,
                vae_path=vae_path,
                width=w, height=h,
                frames=num_frames,
                steps=steps, cfg=cfg,
                sampler=sampler, scheduler=scheduler,
                shift=shift, seed=seed,
            )

            try:
                log(f"  [{count}/{total}] {sid} 提交中...")
                result = _queue_prompt(workflow)
                prompt_id = result.get("prompt_id")
                if not prompt_id:
                    raise RuntimeError(f"API 返回无 prompt_id: {result}")

                log(f"  [{count}/{total}] {sid} 等待完成 (id={prompt_id[:8]}...)")
                completion = _wait_for_completion(prompt_id, timeout=1800)

                # 提取输出视频路径
                outputs = completion.get("outputs", {})
                video_output = None
                for node_id, node_output in outputs.items():
                    # VHS_VideoCombine 输出 key 可能是 "gifs" 或 "vhs_filenames"
                    if "gifs" in node_output:
                        gifs = node_output["gifs"]
                        if isinstance(gifs, list) and len(gifs) > 0:
                            video_output = gifs[0]
                            if isinstance(video_output, dict):
                                video_output = video_output.get("fullpath", video_output.get("filename", ""))
                            break
                    if "vhs_filenames" in node_output:
                        video_output = node_output["vhs_filenames"][0]
                        break

                if video_output and os.path.isfile(video_output):
                    # 复制到目标路径
                    import shutil
                    shutil.copy2(video_output, out)
                    dur = shot.get("duration_seconds", 3)
                    size_mb = os.path.getsize(out) / 1e6
                    log(f"  [{count}/{total}] {sid} ✓ ({num_frames}f, {dur}s, {size_mb:.1f}MB)")
                    # 如果文件太小（<50KB），说明可能是静态帧
                    if size_mb < 0.05:
                        log(f"    ⚠️ 文件极小({size_mb:.3f}MB)，可能是静态帧！")
                        _check_comfyui_logs()
                        # 打印 history 中该 prompt 的详细执行信息
                        _print_prompt_history(prompt_id)
                else:
                    log(f"  [{count}/{total}] {sid} 无输出视频")
                    # 调试：打印完整 outputs 结构
                    log(f"    DEBUG outputs keys: {list(outputs.keys()) if outputs else 'None'}")
                    for nid, nout in (outputs or {}).items():
                        if isinstance(nout, dict):
                            for k, v in nout.items():
                                val_preview = str(v)[:200]
                                log(f"    node[{nid}].{k} = {val_preview}")
                    _save_placeholder_video(shot, out, num_frames)

            except Exception as e:
                log(f"  [{count}/{total}] {sid} 失败: {e}")
                _save_placeholder_video(shot, out, num_frames)

    log("视频生成完成")


def _save_placeholder_video(shot, output_path, num_frames):
    from PIL import Image, ImageDraw
    import shutil
    res = WAN22_DEFAULTS["width"]
    img = Image.new("RGB", (res, res), (20, 20, 40))
    draw = ImageDraw.Draw(img)
    draw.rectangle([5, 5, res - 5, res - 5], outline=(100, 100, 200), width=2)
