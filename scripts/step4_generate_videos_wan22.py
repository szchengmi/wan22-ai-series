def _build_wan22_workflow(positive_prompt, negative_prompt, model_path,
                           clip_path, vae_path, width, height, frames,
                           steps, cfg, sampler, scheduler, shift, seed):
    """
    构建 Wan2.2 T2V ComfyUI 工作流（API 格式）。

  节点:
     1: UnetLoaderGGUF — 加载 GGUF UNET
     2: CLIPLoaderGGUF — 加载 text encoder
     3: VAELoader — 加载 VAE
     4: CLIPTextEncode (positive) — 正面提示词
     5: CLIPTextEncode (negative) — 负面提示词
     6: WanImageToVideo — T2V latent 准备（自动生成空 latent）
     7: KSampler — 去噪采样
     8: VAEDecode — 解码 latent 为像素
     9: VHS_VideoCombine — 保存视频
    """
    # 计算 latent 尺寸
    lat_w = width // 8
    lat_h = height // 8
    lat_frames = (frames - 1) // 4 + 1

    # 只传文件名（ComfyUI 通过 extra_model_paths.yaml 注册路径能找到完整路径）
    unet_filename = os.path.basename(model_path)
    clip_filename = os.path.basename(clip_path)

    workflow = {
        "1": {
            "class_type": "UnetLoaderGGUF",
            "inputs": {"unet_name": unet_filename},
        },
        "2": {
            "class_type": "CLIPLoaderGGUF",
            "inputs": {"clip_name": clip_filename, "type": "wan"},
        },
        "3": {
            "class_type": "VAELoader",
            "inputs": {"vae_name": os.path.basename(vae_path)},
        },
        "4": {
            "class_type": "CLIPTextEncode",
            "inputs": {"text": positive_prompt, "clip": ["2", 0]},
        },
        "5": {
            "class_type": "CLIPTextEncode",
            "inputs": {"text": negative_prompt, "clip": ["2", 0]},
        },
        "6": {
            "class_type": "WanImageToVideo",
            "inputs": {
                "positive": ["4", 0],
                "negative": ["5", 0],
                "vae": ["3", 0],
                "width": width,
                "height": height,
                "length": frames,
                "batch_size": 1,
            },
        },
        "7": {
            "class_type": "KSampler",
            "inputs": {
                "model": ["1", 0],
                "positive": ["6", 0],
                "negative": ["6", 1],
                "latent_image": ["6", 2],
                "seed": seed,
                "steps": steps,
                "cfg": cfg,
                "sampler_name": sampler,
                "scheduler": scheduler,
                "denoise": 1.0,
            },
        },
        "8": {
            "class_type": "VAEDecode",
            "inputs": {
                "samples": ["7", 0],
                "vae": ["3", 0],
            },
        },
        "9": {
            "class_type": "VHS_VideoCombine",
            "inputs": {
                "images": ["8", 0],
                "frame_rate": WAN22_DEFAULTS["fps"],
                "loop_count": 0,
                "filename_prefix": "wan22_output",
                "format": "video/h264-mp4",
                "pingpong": False,
                "save_output": True,
            },
        },
    }
    return workflow


