def _start_comfyui():
    """启动 ComfyUI 服务器（后台）"""
    # 已在运行？
    try:
        urllib.request.urlopen(f"{COMFYUI_URL}/system_stats", timeout=2)
        log("ComfyUI 已在运行")
        return True
    except:
        pass

    # 查找或安装
    result = _find_comfyui()
    if result:
        install_type, path = result
        if install_type == "pip":
            log(f"  ComfyUI (pip): {path}")
        else:
            log(f"  ComfyUI (dir): {path}")
    else:
        _install_comfyui()
        result = _find_comfyui()
        if not result:
            log("❌ ComfyUI 安装后仍无法找到")
            return False
        install_type, path = result

    cwd = path if isinstance(path, str) and os.path.isdir(path) else None
    log("启动 ComfyUI...")
    env = os.environ.copy()
    env["CUDA_VISIBLE_DEVICES"] = "0"

    # 检测 GPU 可用性
    try:
        import torch
        has_gpu = torch.cuda.is_available()
    except:
        has_gpu = False
    gpu_flag = [] if has_gpu else ["--cpu"]
    log(f"  GPU: {'可用' if has_gpu else '不可用(CPU模式)'}")

    # ★ 创建 extra_model_paths.yaml 让 ComfyUI 加载时注册 Dataset 路径 ★
    _create_extra_model_paths(cwd)

    log_file = "/kaggle/working/ai-series/comfyui_startup.log"
    log_fh = open(log_file, "w")   
    if install_type == "pip":
        proc = subprocess.Popen(
            ["python", "-m", "comfy", "main", "--dont-print-server",
             "--preview-method", "none", "--listen", "0.0.0.0", "--port", "8188"] + gpu_flag,
            stdout=log_fh,
            stderr=subprocess.STDOUT,
            env=env,
        )
    else:
        proc = subprocess.Popen(
            ["python", "main.py", "--dont-print-server",
             "--preview-method", "none", "--listen", "0.0.0.0", "--port", "8188"] + gpu_flag,
            cwd=cwd,
            stdout=log_fh,
            stderr=subprocess.STDOUT,
            env=env,
        )

    # 快速轮询（3s 间隔），最多 10 分钟
    for i in range(200):
        time.sleep(3)
        try:
            urllib.request.urlopen(f"{COMFYUI_URL}/system_stats", timeout=2)
            log(f"  ComfyUI 就绪 ({(i + 1) * 3}s)")
            log_fh.close()
            return True
        except:
            if i < 5 or i % 20 == 0:
                log(f"  ⏳ 等待... ({(i + 1) * 3}s)")
    # 超时，打印启动日志
    log_fh.close()
    try:
        with open(log_file, "r") as f:
            last_lines = f.readlines()[-30:]
        log("  ❌ ComfyUI 启动超时，最后30行日志:")
        for line in last_lines:
            log(f"    {line.rstrip()}")
    except:
        log(f"  ❌ ComfyUI 启动超时，无法读取 {log_file}")
    return False


def _queue_prompt(workflow):
    """提交工作流到 ComfyUI API"""
    payload = json.dumps({"prompt": workflow, "client_id": "wan22-ai"}).encode()
    req = urllib.request.Request(
        f"{COMFYUI_URL}/prompt",
        data=payload,
        headers={"Content-Type": "application/json"},
    )
    try:
        with urllib.request.urlopen(req, timeout=600) as resp:
            return json.loads(resp.read())
    except urllib.error.HTTPError as e:
        body = e.read().decode()
        log(f"  ComfyUI 错误: HTTP {e.code}")
        log(f"  请求体(前500): {payload[:500]}")
        log(f"  响应体: {body[:300]}")
        raise
    except Exception as e:
        log(f"  请求体(前500): {payload[:500]}")
        raise


def _get_history(prompt_id):
    """获取工作流执行结果"""
    req = urllib.request.Request(f"{COMFYUI_URL}/history/{prompt_id}")
    with urllib.request.urlopen(req, timeout=60) as resp:
        return json.loads(resp.read())


def _wait_for_completion(prompt_id, timeout=1800):
    """等待工作流执行完成"""
    start = time.time()
    last_log = 0
    while time.time() - start < timeout:
        try:
            history = _get_history(prompt_id)
            if prompt_id in history:
                entry = history[prompt_id]
                status = entry.get("status", {}).get("status_str")
                if status == "success":
                    return entry
                if status == "error":
                    raise RuntimeError(f"工作流执行失败: {entry}")
                # 每30秒打印一次进度
                elapsed = time.time() - start
                if elapsed - last_log >= 30:
                    executors = entry.get("status", {}).get("exec_info", {})
                    queue_remaining = executors.get("queue_remaining", "?")
                    log(f"    ⏳ {elapsed:.0f}s 完成, queue={queue_remaining}")
                    last_log = elapsed
        except urllib.error.HTTPError:
            pass
        time.sleep(5)
    raise TimeoutError(f"工作流 {prompt_id} 超时 ({timeout}s)")


